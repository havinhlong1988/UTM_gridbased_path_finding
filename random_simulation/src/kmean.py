def save_outputs(
    model: LoadedModel,
    params: SimpleNamespace,
    output_dir: Path,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    candidates: pd.DataFrame,
    processed_routes: list[LoadedRoute],
    skipped_routes: list[LoadedRoute],
) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    _write_params_snapshot(params, output_dir)

    if bool(pget(params, "SAVE_NODE_HIT_TABLE", True)):
        node_table = build_node_output_table(model, eligible_mask, hit_count, len(processed_routes))
        node_table.to_csv(output_dir / "all_model_nodes_route_hit_density.csv", index=False)

    if bool(pget(params, "SAVE_NODE_OBJECTIVE_CANDIDATE_TABLE", True)):
        node_obj_cand = build_node_objective_candidate_table(
            model=model,
            params=params,
            eligible_mask=eligible_mask,
            hit_count=hit_count,
            n_processed_routes=len(processed_routes),
            candidates=candidates,
        )
        node_obj_cand.to_csv(output_dir / "all_model_nodes_objective_TNcandidates.csv", index=False)

    if bool(pget(params, "SAVE_CANDIDATE_TABLE", True)):
        candidates.to_csv(output_dir / "traffic_node_candidates_major_minor.csv", index=False)

    if bool(pget(params, "SAVE_ROUTE_SUMMARY_TABLE", True)):
        route_summary = pd.concat([
            _routes_to_table(processed_routes),
            _routes_to_table(skipped_routes),
        ], ignore_index=True)
        route_summary.to_csv(output_dir / "route_file_processing_summary.csv", index=False)

    make_figures(model, params, output_dir, eligible_mask, hit_count, candidates, processed_routes)


