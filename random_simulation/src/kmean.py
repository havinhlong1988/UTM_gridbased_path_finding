def _threshold_pool_by_hit(
    hit_count: np.ndarray,
    eligible_mask: np.ndarray,
    min_hit_count: int,
    percentile: float,
    min_needed: int,
) -> np.ndarray:
    eligible_indices = np.flatnonzero(eligible_mask)
    hits = hit_count[eligible_indices]

    pool = eligible_indices[hits >= int(min_hit_count)]
    if len(pool) >= min_needed:
        return pool

    positive = hits[hits > 0]
    if positive.size > 0:
        pct = float(np.clip(percentile, 0.0, 100.0))
        threshold = np.percentile(positive, pct)
        pool = eligible_indices[hits >= threshold]
        if len(pool) >= min_needed:
            return pool

    # Last fallback: use top hit nodes among eligible nodes.
    order = np.argsort(-hits)
    take = min(len(order), max(min_needed, 1))
    return eligible_indices[order[:take]]


def _minor_pool(
    hit_count: np.ndarray,
    eligible_mask: np.ndarray,
    params: SimpleNamespace,
    min_needed: int,
) -> np.ndarray:
    mode = str(pget(params, "MINOR_POOL_MODE", "all_eligible")).lower()
    eligible_indices = np.flatnonzero(eligible_mask)
    hits = hit_count[eligible_indices]

    if mode == "all_eligible":
        return eligible_indices
    if mode == "hit_positive":
        pool = eligible_indices[hits >= int(pget(params, "MINOR_MIN_HIT_COUNT", 1))]
        if len(pool) >= min_needed:
            return pool
        return eligible_indices[np.argsort(-hits)[: min(len(eligible_indices), min_needed)]]
    if mode == "percentile":
        return _threshold_pool_by_hit(
            hit_count=hit_count,
            eligible_mask=eligible_mask,
            min_hit_count=int(pget(params, "MINOR_MIN_HIT_COUNT", 1)),
            percentile=float(pget(params, "MINOR_HIT_PERCENTILE", 40.0)),
            min_needed=min_needed,
        )
    raise ValueError(f"Unknown MINOR_POOL_MODE: {mode}")


def _sample_weights(hit_count: np.ndarray, indices: np.ndarray, params: SimpleNamespace) -> np.ndarray:
    power = float(pget(params, "HIT_WEIGHT_POWER", 1.0))
    hits = np.asarray(hit_count[indices], dtype=float)
    return 1.0 + np.power(np.maximum(hits, 0.0), power)


def _too_close_to_existing(
    xy_metric: np.ndarray,
    idx: int,
    selected_indices: list[int],
    min_sep_m: float,
) -> bool:
    if min_sep_m <= 0 or not selected_indices:
        return False
    p = xy_metric[idx]
    existing = xy_metric[np.asarray(selected_indices, dtype=int)]
    d = np.linalg.norm(existing - p, axis=1)
    return bool(np.any(d < min_sep_m))


def _select_cluster_representative(
    cluster_indices: np.ndarray,
    center: np.ndarray,
    model: LoadedModel,
    hit_count: np.ndarray,
    params: SimpleNamespace,
    selected_indices: list[int],
    min_sep_m: float,
) -> int | None:
    method = str(pget(params, "REPRESENTATIVE_METHOD", "max_hit_then_center")).lower()
    xy = model.xy_metric[cluster_indices]
    dist = np.linalg.norm(xy - center, axis=1)
    hits = hit_count[cluster_indices]

    if method == "nearest_center":
        order = np.argsort(dist)
    elif method == "max_hit_then_center":
        # Highest hit first; tie-break by nearest center.
        order = np.lexsort((dist, -hits))
    else:
        raise ValueError(f"Unknown REPRESENTATIVE_METHOD: {method}")

    for local in order:
        idx = int(cluster_indices[local])
        if not _too_close_to_existing(model.xy_metric, idx, selected_indices, min_sep_m):
            return idx
    return None


def _fit_kmeans_candidates(
    pool_indices: np.ndarray,
    n_candidates: int,
    candidate_type: str,
    model: LoadedModel,
    hit_count: np.ndarray,
    params: SimpleNamespace,
    already_selected: list[int] | None = None,
) -> list[dict]:
    if KMeans is None:
        raise ImportError(
            "scikit-learn is required for KMeans. Install with: pip install scikit-learn"
        ) from _SKLEARN_IMPORT_ERROR

    pool_indices = np.asarray(pool_indices, dtype=int)
    pool_indices = np.unique(pool_indices)
    if already_selected is None:
        already_selected = []

    # Remove nodes that are too close to already selected candidates before KMeans.
    min_sep_m = float(pget(params, "CANDIDATE_MIN_SEPARATION_M", 300.0))
    if already_selected and min_sep_m > 0:
        selected_xy = model.xy_metric[np.asarray(already_selected, dtype=int)]
        tree = cKDTree(selected_xy)
        dist, _ = tree.query(model.xy_metric[pool_indices], k=1)
        pool_indices = pool_indices[dist >= min_sep_m]

    if len(pool_indices) == 0 or n_candidates <= 0:
        return []

    n_clusters = min(int(n_candidates), len(pool_indices))
    xy = model.xy_metric[pool_indices]
    weights = _sample_weights(hit_count, pool_indices, params)

    kmeans = KMeans(
        n_clusters=n_clusters,
        random_state=int(pget(params, "KMEANS_RANDOM_STATE", 42)),
        n_init=int(pget(params, "KMEANS_N_INIT", 20)),
        max_iter=int(pget(params, "KMEANS_MAX_ITER", 300)),
    )
    labels = kmeans.fit_predict(xy, sample_weight=weights)

    selected = list(already_selected)
    rows: list[dict] = []
    for cluster_id in range(n_clusters):
        members = pool_indices[labels == cluster_id]
        if len(members) == 0:
            continue
        center = kmeans.cluster_centers_[cluster_id]
        idx = _select_cluster_representative(
            members,
            center,
            model=model,
            hit_count=hit_count,
            params=params,
            selected_indices=selected,
            min_sep_m=min_sep_m,
        )
        if idx is None:
            continue
        selected.append(idx)
        center_dist = float(np.linalg.norm(model.xy_metric[idx] - center))
        rows.append(
            {
                "candidate_type": candidate_type,
                "cluster_id": int(cluster_id),
                "model_index": int(idx),
                "x": float(model.xy_original[idx, 0]),
                "y": float(model.xy_original[idx, 1]),
                "x_m": float(model.xy_metric[idx, 0]),
                "y_m": float(model.xy_metric[idx, 1]),
                "z": float(model.df[model.z_col].iloc[idx]) if model.z_col is not None and pd.notna(model.df[model.z_col].iloc[idx]) else np.nan,
                "slowness": float(model.df[model.slowness_col].iloc[idx]) if model.slowness_col is not None and pd.notna(model.df[model.slowness_col].iloc[idx]) else np.nan,
                "label": str(model.df[model.label_col].iloc[idx]) if model.label_col is not None else "",
                "label_prefix": str(model.df[model.label_prefix_col].iloc[idx]) if model.label_prefix_col is not None else "",
                "route_hit_count": int(hit_count[idx]),
                "cluster_member_count": int(len(members)),
                "cluster_hit_sum": int(hit_count[members].sum()),
                "distance_to_kmean_center_m": center_dist,
            }
        )

    # Sort candidates by type priority and hit count for cleaner output.
    rows.sort(key=lambda r: (-int(r["route_hit_count"]), int(r["cluster_id"])))
    return rows


def _auto_count_base_size(
    base_name: str,
    *,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    pool_indices: np.ndarray | None = None,
) -> int:
    """Return denominator for automatic candidate-count percentage.

    Supported base names:
        eligible     : all eligible candidate nodes
        hit_positive : eligible nodes with route_hit_count > 0
        pool         : the current candidate pool passed to KMeans
    """
    base = str(base_name or "eligible").strip().lower()
    eligible_indices = np.flatnonzero(eligible_mask)

    if base in ("eligible", "all_eligible", "all"):
        return int(len(eligible_indices))

    if base in ("hit_positive", "positive", "hit", "route_hit"):
        if len(eligible_indices) == 0:
            return 0
        return int(np.count_nonzero(hit_count[eligible_indices] > 0))

    if base in ("pool", "candidate_pool", "kmeans_pool"):
        return int(0 if pool_indices is None else len(np.unique(np.asarray(pool_indices, dtype=int))))

    raise ValueError(
        f"Unknown automatic count base: {base_name}. "
        "Use eligible, hit_positive, or pool."
    )


def _count_from_percentage(
    *,
    percent: float,
    base_size: int,
    min_count: int,
    max_count: int,
    fallback_count: int,
) -> int:
    """Convert a percent value into a safe candidate count.

    Important: percent is written as percent, not fraction.
    Example: 0.10 means 0.10% of the chosen base.
    """
    try:
        pct = float(percent)
    except Exception:
        pct = 0.0

    base_size = max(0, int(base_size))
    min_count = max(0, int(min_count))
    max_count = max(0, int(max_count))
    fallback_count = max(0, int(fallback_count))

    if base_size <= 0 or pct <= 0.0:
        n = fallback_count
    else:
        n = int(math.ceil(base_size * pct / 100.0))

    if min_count > 0:
        n = max(n, min_count)
    if max_count > 0:
        n = min(n, max_count)
    if base_size > 0:
        n = min(n, base_size)
    return max(0, int(n))


def _auto_candidate_counts_from_percentage(
    *,
    params: SimpleNamespace,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    major_pool: np.ndarray,
    minor_pool: np.ndarray,
) -> tuple[int, int, dict[str, object]]:
    """Auto-define major/minor candidate numbers when both manual counts are 0.

    Rule requested for v8:
        if N_MAJOR_CANDIDATES == 0 and N_MINOR_CANDIDATES == 0:
            use percentage-based automatic candidate counts.

    The percentages are controlled by params and can use different bases for
    major and minor candidates. This keeps the old manual option untouched.
    """
    major_base_name = str(pget(params, "AUTO_MAJOR_COUNT_BASE", "hit_positive"))
    minor_base_name = str(pget(params, "AUTO_MINOR_COUNT_BASE", "eligible"))

    major_base_size = _auto_count_base_size(
        major_base_name,
        eligible_mask=eligible_mask,
        hit_count=hit_count,
        pool_indices=major_pool,
    )
    minor_base_size = _auto_count_base_size(
        minor_base_name,
        eligible_mask=eligible_mask,
        hit_count=hit_count,
        pool_indices=minor_pool,
    )

    n_major = _count_from_percentage(
        percent=float(pget(params, "AUTO_MAJOR_COUNT_PERCENT", 0.20)),
        base_size=major_base_size,
        min_count=int(pget(params, "AUTO_MAJOR_MIN_CANDIDATES", 3)),
        max_count=int(pget(params, "AUTO_MAJOR_MAX_CANDIDATES", 40)),
        fallback_count=int(pget(params, "AUTO_MAJOR_FALLBACK_CANDIDATES", 8)),
    )
    n_minor = _count_from_percentage(
        percent=float(pget(params, "AUTO_MINOR_COUNT_PERCENT", 0.15)),
        base_size=minor_base_size,
        min_count=int(pget(params, "AUTO_MINOR_MIN_CANDIDATES", 5)),
        max_count=int(pget(params, "AUTO_MINOR_MAX_CANDIDATES", 80)),
        fallback_count=int(pget(params, "AUTO_MINOR_FALLBACK_CANDIDATES", 12)),
    )

    # Never ask KMeans for more clusters than available pool points.
    n_major = min(n_major, int(len(np.unique(np.asarray(major_pool, dtype=int)))))
    n_minor = min(n_minor, int(len(np.unique(np.asarray(minor_pool, dtype=int)))))

    meta = {
        "mode": "auto_percentage",
        "major_count_base": major_base_name,
        "major_base_size": int(major_base_size),
        "major_percent": float(pget(params, "AUTO_MAJOR_COUNT_PERCENT", 0.20)),
        "major_count": int(n_major),
        "minor_count_base": minor_base_name,
        "minor_base_size": int(minor_base_size),
        "minor_percent": float(pget(params, "AUTO_MINOR_COUNT_PERCENT", 0.15)),
        "minor_count": int(n_minor),
    }
    return int(n_major), int(n_minor), meta


def select_candidates(
    model: LoadedModel,
    params: SimpleNamespace,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
) -> pd.DataFrame:
    requested_n_major = int(pget(params, "N_MAJOR_CANDIDATES", 12))
    requested_n_minor = int(pget(params, "N_MINOR_CANDIDATES", 20))
    min_sep_m = float(pget(params, "CANDIDATE_MIN_SEPARATION_M", 300.0))

    # v8 behavior:
    #   - normal/manual mode: use N_MAJOR_CANDIDATES and N_MINOR_CANDIDATES
    #   - auto mode only when BOTH are zero: derive counts from percentages
    use_auto_percentage_counts = (requested_n_major == 0 and requested_n_minor == 0)

    major_min_needed = 1 if use_auto_percentage_counts else max(requested_n_major, 1)
    major_pool = _threshold_pool_by_hit(
        hit_count=hit_count,
        eligible_mask=eligible_mask,
        min_hit_count=int(pget(params, "MAJOR_MIN_HIT_COUNT", 2)),
        percentile=float(pget(params, "MAJOR_HIT_PERCENTILE", 80.0)),
        min_needed=major_min_needed,
    )

    minor_min_needed = 1 if use_auto_percentage_counts else max(requested_n_minor, 1)
    minor_pool = _minor_pool(hit_count, eligible_mask, params, min_needed=minor_min_needed)

    if use_auto_percentage_counts:
        n_major, n_minor, auto_meta = _auto_candidate_counts_from_percentage(
            params=params,
            eligible_mask=eligible_mask,
            hit_count=hit_count,
            major_pool=major_pool,
            minor_pool=minor_pool,
        )
        print(
            "[kmean] Auto candidate count mode: "
            f"major={n_major} "
            f"({auto_meta['major_percent']}% of {auto_meta['major_count_base']}={auto_meta['major_base_size']}), "
            f"minor={n_minor} "
            f"({auto_meta['minor_percent']}% of {auto_meta['minor_count_base']}={auto_meta['minor_base_size']})"
        )
    else:
        n_major = max(0, int(requested_n_major))
        n_minor = max(0, int(requested_n_minor))
        print(f"[kmean] Manual candidate count mode: major={n_major}, minor={n_minor}")

    major_rows = _fit_kmeans_candidates(
        major_pool,
        n_major,
        "major",
        model=model,
        hit_count=hit_count,
        params=params,
        already_selected=[],
    )

    selected_major = [int(r["model_index"]) for r in major_rows]

    # Minor candidates are intended as coverage/back-up nodes, so remove major
    # nodes and optionally remove the immediate major neighborhood.
    if selected_major:
        minor_pool = np.setdiff1d(minor_pool, np.asarray(selected_major, dtype=int), assume_unique=False)
        if min_sep_m > 0 and len(minor_pool) > 0:
            tree = cKDTree(model.xy_metric[np.asarray(selected_major, dtype=int)])
            dist, _ = tree.query(model.xy_metric[minor_pool], k=1)
            minor_pool = minor_pool[dist >= min_sep_m]

    minor_rows = _fit_kmeans_candidates(
        minor_pool,
        n_minor,
        "minor",
        model=model,
        hit_count=hit_count,
        params=params,
        already_selected=selected_major,
    )

    rows = major_rows + minor_rows
    if not rows:
        return pd.DataFrame()

    out = pd.DataFrame(rows)
    out.insert(0, "candidate_id", [f"TN{i + 1:03d}" for i in range(len(out))])
    type_rank = out["candidate_type"].map({"major": 0, "minor": 1}).fillna(9)
    out = out.assign(_rank=type_rank).sort_values(["_rank", "route_hit_count"], ascending=[True, False]).drop(columns="_rank")
    out["candidate_id"] = [f"TN{i + 1:03d}" for i in range(len(out))]
    return out.reset_index(drop=True)


