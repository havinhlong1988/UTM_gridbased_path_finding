def run_route_hit_density_kmeans(params: SimpleNamespace) -> pd.DataFrame:
    output_dir = Path(pget(params, "OUTPUT_DIR", "output/kmean_route_hit_density"))
    output_dir.mkdir(parents=True, exist_ok=True)

    print(f"[kmean] src.kmean module version: {MODULE_VERSION}")
    print("[kmean] Loading raw model nodes...")
    model = load_model(params)
    print(f"[kmean] Model nodes: {len(model.df):,}")
    print(f"[kmean] Coordinate mode: {model.metric_mode}")

    print("[kmean] Building candidate eligibility mask...")
    eligible_mask = build_eligible_mask(model, params)
    print(f"[kmean] Eligible candidate nodes: {int(eligible_mask.sum()):,}")

    print("[kmean] Counting route hits around model nodes...")
    hit_count, processed_routes, skipped_routes = compute_route_hits(model, params, eligible_mask)
    print(f"[kmean] Processed route files: {len(processed_routes):,}")
    print(f"[kmean] Skipped route files: {len(skipped_routes):,}")
    if skipped_routes:
        print("[kmean] First skipped route files / reasons:")
        for r in skipped_routes[:10]:
            print(f"[kmean]   skipped {r.path.name}: {r.reason}")
    print(f"[kmean] Max route-hit count: {int(hit_count.max())}")

    print("[kmean] Running KMeans for major/minor traffic candidates...")
    candidates = select_candidates(model, params, eligible_mask, hit_count)

    print("[kmean] Checking candidate clearance from no-fly/obstacle nodes...")
    candidates = adjust_candidates_for_nofly_clearance(model, params, eligible_mask, hit_count, candidates)

    print(f"[kmean] Selected traffic-node candidates: {len(candidates):,}")
    if not candidates.empty:
        cols = ["candidate_id", "candidate_type", "model_index", "x", "y", "route_hit_count", "nofly_clearance_m", "candidate_shifted"]
        cols = [c for c in cols if c in candidates.columns]
        print(candidates[cols].to_string(index=False))

    print("[kmean] Saving outputs...")
    save_outputs(model, params, output_dir, eligible_mask, hit_count, candidates, processed_routes, skipped_routes)
    print(f"[kmean] Done. Output directory: {output_dir}")
    return candidates
