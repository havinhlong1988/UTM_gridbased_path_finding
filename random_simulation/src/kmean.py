def build_nofly_mask(model: LoadedModel, params: SimpleNamespace) -> np.ndarray:
    """Mask model nodes that are no-fly by slowness threshold only.

    This is separate from eligible_mask because eligible_mask also excludes
    objectives (DB/DK/FLZ/RA).  For visual checking on Figure 04, the user
    specifically requested the no-fly zone overlay.
    """
    mask = _finite_xy_mask(model.xy_metric)
    if model.slowness_col is None:
        return np.zeros(len(model.df), dtype=bool)

    slow = _coerce_numeric(model.df[model.slowness_col]).to_numpy(dtype=float)
    threshold = float(pget(params, "NOFLY_SLOWNESS_THRESHOLD", 10.0))
    mode = str(pget(params, "NOFLY_THRESHOLD_MODE", "greater_equal")).lower()
    if mode == "greater_equal":
        mask &= slow >= threshold
    elif mode == "greater":
        mask &= slow > threshold
    elif mode == "equal":
        mask &= np.isclose(slow, threshold)
    else:
        raise ValueError(f"Unknown NOFLY_THRESHOLD_MODE: {mode}")
    return mask


def _candidate_row_from_model_index(
    row: pd.Series,
    idx: int,
    model: LoadedModel,
    hit_count: np.ndarray,
) -> dict:
    """Return candidate row values updated to a new model node index."""
    idx = int(idx)
    out = dict(row)
    out["model_index"] = idx
    out["x"] = float(model.xy_original[idx, 0])
    out["y"] = float(model.xy_original[idx, 1])
    out["x_m"] = float(model.xy_metric[idx, 0])
    out["y_m"] = float(model.xy_metric[idx, 1])
    out["z"] = (
        float(model.df[model.z_col].iloc[idx])
        if model.z_col is not None and pd.notna(model.df[model.z_col].iloc[idx])
        else np.nan
    )
    out["slowness"] = (
        float(model.df[model.slowness_col].iloc[idx])
        if model.slowness_col is not None and pd.notna(model.df[model.slowness_col].iloc[idx])
        else np.nan
    )
    out["label"] = str(model.df[model.label_col].iloc[idx]) if model.label_col is not None else ""
    out["label_prefix"] = str(model.df[model.label_prefix_col].iloc[idx]) if model.label_prefix_col is not None else ""
    out["route_hit_count"] = int(hit_count[idx])
    return out


def _distance_to_nofly_nodes_m(model: LoadedModel, nofly_mask: np.ndarray) -> np.ndarray:
    """Return nearest no-fly distance for every model node in metric meters."""
    dist = np.full(len(model.df), np.inf, dtype=float)
    nofly_idx = np.flatnonzero(nofly_mask)
    finite_nodes = _finite_xy_mask(model.xy_metric)
    if len(nofly_idx) == 0:
        return dist
    tree = cKDTree(model.xy_metric[nofly_idx])
    qidx = np.flatnonzero(finite_nodes)
    qdist, _ = tree.query(model.xy_metric[qidx], k=1)
    dist[qidx] = qdist
    return dist


def adjust_candidates_for_nofly_clearance(
    model: LoadedModel,
    params: SimpleNamespace,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    candidates: pd.DataFrame,
) -> pd.DataFrame:
    """Shift traffic-node candidates away from nearby no-fly/obstacle nodes.

    Logic
    -----
    KMeans finds high route-hit-density representatives, but some selected raw
    nodes can sit very close to no-fly/obstacle cells.  For a practical traffic
    node, surrounding free space matters.  This post-processing step checks the
    nearest no-fly distance and, only when a candidate is too close, searches a
    nearby eligible replacement node with better clearance.

    The original node is kept when no better surrounding-space candidate exists.
    """
    if candidates is None or candidates.empty:
        return candidates

    enabled = bool(pget(params, "ENABLE_CANDIDATE_CLEARANCE_SHIFT", True))
    min_clearance_m = float(pget(params, "CANDIDATE_MIN_NOFLY_CLEARANCE_M", 150.0))
    search_radius_m = float(pget(params, "CANDIDATE_SHIFT_SEARCH_RADIUS_M", 600.0))
    min_sep_m = float(pget(params, "CANDIDATE_SHIFT_MIN_SEPARATION_M", pget(params, "CANDIDATE_MIN_SEPARATION_M", 300.0)))
    max_hit_loss_fraction = float(pget(params, "CANDIDATE_SHIFT_MAX_HIT_LOSS_FRACTION", 1.0))
    clearance_weight = float(pget(params, "CANDIDATE_SHIFT_CLEARANCE_WEIGHT", 1.0))
    hit_weight = float(pget(params, "CANDIDATE_SHIFT_HIT_WEIGHT", 0.25))
    distance_penalty = float(pget(params, "CANDIDATE_SHIFT_DISTANCE_PENALTY", 0.20))
    require_clearance_target = bool(pget(params, "CANDIDATE_SHIFT_REQUIRE_CLEARANCE_TARGET", True))
    enforce_separation = bool(pget(params, "CANDIDATE_SHIFT_ENFORCE_CANDIDATE_SEPARATION", True))

    nofly_mask = build_nofly_mask(model, params)
    nofly_clearance = _distance_to_nofly_nodes_m(model, nofly_mask)
    eligible_indices = np.flatnonzero(eligible_mask)

    if len(eligible_indices) == 0 or not np.any(nofly_mask):
        out = candidates.copy()
        out["original_model_index"] = out["model_index"].astype(int)
        out["shifted_from_model_index"] = ""
        out["candidate_shifted"] = False
        out["candidate_shift_distance_m"] = 0.0
        out["original_nofly_clearance_m"] = np.inf
        out["nofly_clearance_m"] = np.inf
        out["shift_reason"] = "no no-fly nodes found"
        return out

    eligible_tree = cKDTree(model.xy_metric[eligible_indices])
    rows = []
    original_indices = candidates["model_index"].astype(int).tolist()
    final_indices: list[int] = []
    shifted_count = 0

    for pos, (_, row) in enumerate(candidates.iterrows()):
        orig_idx = int(row["model_index"])
        orig_clearance = float(nofly_clearance[orig_idx])
        orig_hit = int(hit_count[orig_idx])

        base = dict(row)
        base.setdefault("original_model_index", orig_idx)
        base["original_model_index"] = orig_idx
        base["shifted_from_model_index"] = ""
        base["candidate_shifted"] = False
        base["candidate_shift_distance_m"] = 0.0
        base["original_nofly_clearance_m"] = orig_clearance
        base["nofly_clearance_m"] = orig_clearance
        base["shift_search_candidate_count"] = 0
        base["shift_selected_score"] = np.nan
        base["shift_reason"] = "clearance ok"

        if (not enabled) or (not math.isfinite(orig_clearance)) or orig_clearance >= min_clearance_m:
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        local = eligible_tree.query_ball_point(model.xy_metric[orig_idx], r=search_radius_m)
        if not local:
            base["shift_reason"] = "kept: no eligible nodes inside search radius"
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        cand_idx = eligible_indices[np.asarray(local, dtype=int)]
        cand_idx = cand_idx[cand_idx != orig_idx]
        if len(cand_idx) == 0:
            base["shift_reason"] = "kept: no alternative eligible nodes inside search radius"
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        # Prefer nodes that satisfy the clearance target.  If this is disabled,
        # allow any node that improves clearance.
        if require_clearance_target:
            cand_idx = cand_idx[nofly_clearance[cand_idx] >= min_clearance_m]
        else:
            cand_idx = cand_idx[nofly_clearance[cand_idx] > orig_clearance]

        if len(cand_idx) == 0:
            base["shift_reason"] = "kept: no nearby node improves required no-fly clearance"
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        # Optionally prevent shifting onto another existing/future candidate.
        if enforce_separation and min_sep_m > 0:
            other_indices = list(final_indices) + [int(v) for v in original_indices[pos + 1 :]]
            other_indices = [v for v in other_indices if v != orig_idx]
            if other_indices:
                other_xy = model.xy_metric[np.asarray(other_indices, dtype=int)]
                other_tree = cKDTree(other_xy)
                dist_other, _ = other_tree.query(model.xy_metric[cand_idx], k=1)
                cand_idx = cand_idx[dist_other >= min_sep_m]

        if len(cand_idx) == 0:
            base["shift_reason"] = "kept: clearance alternatives violate candidate separation"
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        # Allow lower traffic density, but optionally reject extreme hit loss.
        # max_hit_loss_fraction=1.0 means allow down to zero hits.
        min_allowed_hit = max(0.0, float(orig_hit) * (1.0 - max(0.0, min(1.0, max_hit_loss_fraction))))
        cand_idx = cand_idx[hit_count[cand_idx] >= min_allowed_hit]
        if len(cand_idx) == 0:
            base["shift_reason"] = "kept: all clearance alternatives lose too much route-hit density"
            rows.append(base)
            final_indices.append(orig_idx)
            continue

        shift_dist = np.linalg.norm(model.xy_metric[cand_idx] - model.xy_metric[orig_idx], axis=1)
        clearance_norm = np.minimum(nofly_clearance[cand_idx] / max(min_clearance_m, 1.0), 2.0) / 2.0
        hit_norm = hit_count[cand_idx].astype(float) / max(float(hit_count[cand_idx].max()), float(orig_hit), 1.0)
        dist_norm = shift_dist / max(search_radius_m, 1.0)
        score = clearance_weight * clearance_norm + hit_weight * hit_norm - distance_penalty * dist_norm

        best_local = int(np.argmax(score))
        new_idx = int(cand_idx[best_local])
        new_clearance = float(nofly_clearance[new_idx])
        new_row = _candidate_row_from_model_index(row, new_idx, model, hit_count)
        new_row["original_model_index"] = orig_idx
        new_row["shifted_from_model_index"] = orig_idx
        new_row["candidate_shifted"] = True
        new_row["candidate_shift_distance_m"] = float(shift_dist[best_local])
        new_row["original_nofly_clearance_m"] = orig_clearance
        new_row["nofly_clearance_m"] = new_clearance
        new_row["shift_search_candidate_count"] = int(len(cand_idx))
        new_row["shift_selected_score"] = float(score[best_local])
        new_row["shift_reason"] = (
            f"shifted: clearance {orig_clearance:.1f} m -> {new_clearance:.1f} m; "
            f"hit {orig_hit} -> {int(hit_count[new_idx])}"
        )
        rows.append(new_row)
        final_indices.append(new_idx)
        shifted_count += 1

    out = pd.DataFrame(rows)
    # Preserve candidate order and IDs after shifting.
    if "candidate_id" in candidates.columns:
        out["candidate_id"] = candidates["candidate_id"].to_numpy()

    close_count = int(np.count_nonzero(out["original_nofly_clearance_m"].to_numpy(dtype=float) < min_clearance_m))
    print(
        "[kmean] Candidate no-fly clearance adjustment: "
        f"checked={len(out)}, close={close_count}, shifted={shifted_count}, "
        f"target_clearance={min_clearance_m:.1f} m"
    )
    if shifted_count > 0:
        cols = [
            "candidate_id",
            "candidate_type",
            "original_model_index",
            "model_index",
            "candidate_shift_distance_m",
            "original_nofly_clearance_m",
            "nofly_clearance_m",
            "route_hit_count",
        ]
        show = [c for c in cols if c in out.columns]
        print(out.loc[out["candidate_shifted"].astype(bool), show].to_string(index=False))
    return out.reset_index(drop=True)


def build_node_output_table(
    model: LoadedModel,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    n_processed_routes: int,
) -> pd.DataFrame:
    df = model.df.copy()
    df["model_index"] = np.arange(len(df), dtype=int)
    df["x_original"] = model.xy_original[:, 0]
    df["y_original"] = model.xy_original[:, 1]
    df["x_m"] = model.xy_metric[:, 0]
    df["y_m"] = model.xy_metric[:, 1]
    df["candidate_eligible"] = eligible_mask
    df["route_hit_count"] = hit_count
    df["route_hit_fraction"] = hit_count / max(float(n_processed_routes), 1.0)
    return df


def build_node_objective_candidate_table(
    model: LoadedModel,
    params: SimpleNamespace,
    eligible_mask: np.ndarray,
    hit_count: np.ndarray,
    n_processed_routes: int,
    candidates: pd.DataFrame,
) -> pd.DataFrame:
    """Build a full node table with objective and TN-candidate flags.

    Output intention
    ----------------
    Save one whole-map table that contains:
        - all raw model nodes
        - objective flags/types (DB, DK, FLZ, RA by default)
        - TN candidate flags/types (major/minor)

    This is useful for later network planning and auditing.
    """
    df = build_node_output_table(model, eligible_mask, hit_count, n_processed_routes)

    label_col = model.label_col
    prefix_col = model.label_prefix_col

    labels = df[label_col].fillna("").astype(str) if label_col in df.columns else pd.Series([""] * len(df))
    prefixes = df[prefix_col].fillna("").astype(str).str.upper() if prefix_col in df.columns else pd.Series([""] * len(df))

    objective_prefixes = {
        str(v).upper()
        for v in _as_list(pget(params, "OBJECTIVE_LABEL_PREFIXES", ["DB", "DK", "FLZ", "RA"]))
    }
    is_objective = prefixes.isin(objective_prefixes)
    df["is_objective"] = is_objective.to_numpy()
    df["objective_label"] = np.where(is_objective.to_numpy(), labels.to_numpy(dtype=object), "")
    df["objective_prefix"] = np.where(is_objective.to_numpy(), prefixes.to_numpy(dtype=object), "")

    if candidates is None or candidates.empty:
        df["is_candidate"] = False
        df["candidate_id"] = ""
        df["candidate_type"] = ""
        df["candidate_route_hit_count"] = np.nan
        return df

    # Keep useful candidate condition/shift fields in the merged whole-map table.
    preferred_cols = [
        "model_index",
        "candidate_id",
        "candidate_type",
        "route_hit_count",
        "original_model_index",
        "shifted_from_model_index",
        "candidate_shifted",
        "candidate_shift_distance_m",
        "original_nofly_clearance_m",
        "nofly_clearance_m",
        "shift_reason",
    ]
    cand_cols = [c for c in preferred_cols if c in candidates.columns]
    cand_df = candidates[cand_cols].copy()
    if "route_hit_count" in cand_df.columns:
        cand_df = cand_df.rename(columns={"route_hit_count": "candidate_route_hit_count"})
    df = df.merge(cand_df, on="model_index", how="left")
    df["is_candidate"] = df["candidate_id"].notna()
    df["candidate_id"] = df["candidate_id"].fillna("")
    df["candidate_type"] = df["candidate_type"].fillna("")
    if "candidate_shifted" in df.columns:
        df["candidate_shifted"] = df["candidate_shifted"].fillna(False)
    return df


