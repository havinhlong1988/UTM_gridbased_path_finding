def _overlay_kmean_candidates(ax, candidates: pd.DataFrame, params: SimpleNamespace, use_original: bool) -> None:
    """Overlay KMeans major/minor candidates with the same symbol logic as Figure 02."""
    if candidates is None or candidates.empty:
        return

    plot_major_candidates = bool(pget(params, "PLOT_KMEAN_MAJOR_CANDIDATES", True))
    plot_minor_candidates = bool(pget(params, "PLOT_KMEAN_MINOR_CANDIDATES", True))
