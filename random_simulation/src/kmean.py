def _as_list(value) -> list:
    if value is None:
        return []
    if isinstance(value, (list, tuple, set)):
        return list(value)
    return [value]


def _norm_col(name: str) -> str:
    return str(name).strip().lower().replace(" ", "_")


def _find_column(df: pd.DataFrame, candidates: Sequence[str]) -> str | None:
    """Find a column using case-insensitive exact matching first, then relaxed.

    This relaxed function is kept for the raw model file because model-column
    names are controlled by our own scripts. Do not use it for route geometry:
    route summary files often contain columns such as start_x/end_x/center_x,
    which must not be interpreted as route polylines.
    """
    if df is None or df.empty:
        return None
    lookup = {_norm_col(c): c for c in df.columns}
    for cand in candidates:
        key = _norm_col(cand)
        if key in lookup:
            return lookup[key]

    # relaxed partial match, useful for model columns like route_x_m or model_y_m
    norm_cols = [(_norm_col(c), c) for c in df.columns]
    for cand in candidates:
        key = _norm_col(cand)
        for norm, original in norm_cols:
            if norm == key or norm.endswith("_" + key) or norm.startswith(key + "_"):
                return original
    return None


def _find_column_exact(df: pd.DataFrame, candidates: Sequence[str]) -> str | None:
    """Find a column by case-insensitive exact normalized name only."""
    if df is None or df.empty:
        return None
    lookup = {_norm_col(c): c for c in df.columns}
    for cand in candidates:
        key = _norm_col(cand)
        if key in lookup:
            return lookup[key]
    return None


def _coerce_numeric(series: pd.Series) -> pd.Series:
    return pd.to_numeric(series, errors="coerce")


def _looks_like_headerless_numeric(df: pd.DataFrame) -> bool:
    if df.empty:
        return False
    try:
        numeric_cols = sum(pd.to_numeric(df.iloc[:, i], errors="coerce").notna().mean() > 0.8 for i in range(min(3, df.shape[1])))
        return numeric_cols >= 2
    except Exception:
        return False


def _read_table_flexible(path: str | Path) -> pd.DataFrame:
    """Read CSV/whitespace table with or without header."""
    path = Path(path)

    # First try header-aware read. sep=None handles comma/semicolon/tab reasonably.
    attempts = [
        dict(sep=None, engine="python", comment="#"),
        dict(sep=r"\s+", engine="python", comment="#"),
    ]

    for kwargs in attempts:
        try:
            df = pd.read_csv(path, **kwargs)
            if df.shape[1] >= 2:
                return df
        except Exception:
            pass

    # Headerless fallback.
    for kwargs in attempts:
        try:
            df = pd.read_csv(path, header=None, **kwargs)
            if df.shape[1] >= 2 and _looks_like_headerless_numeric(df):
                names = ["x", "y", "z", "slowness", "label", "label_prefix"]
                extra = [f"col_{i}" for i in range(max(0, df.shape[1] - len(names)))]
                df.columns = (names + extra)[: df.shape[1]]
                return df
        except Exception:
            pass

    raise ValueError(f"Could not read table: {path}")


def _derive_label_prefix(label: object) -> str:
    s = "" if pd.isna(label) else str(label).strip()
    if not s:
        return ""
    match = re.match(r"([A-Za-z_]+)", s)
    return match.group(1).upper() if match else s.upper()


def _is_lonlat_xy(xy: np.ndarray) -> bool:
    """Heuristic: true if coordinates look like lon/lat degrees."""
    if xy.size == 0:
        return False
    x = xy[:, 0]
    y = xy[:, 1]
    finite = np.isfinite(x) & np.isfinite(y)
    if finite.sum() == 0:
        return False
    x = x[finite]
    y = y[finite]

    # Hoa Lac-like data are around lon 105, lat 21. Keep it general enough.
    if np.nanmin(x) >= -180 and np.nanmax(x) <= 180 and np.nanmin(y) >= -90 and np.nanmax(y) <= 90:
        # If the span is degree-like, not meter-like.
        if (np.nanmax(x) - np.nanmin(x)) <= 5.0 and (np.nanmax(y) - np.nanmin(y)) <= 5.0:
            return True
    return False


def _lonlat_to_local_m(xy_lonlat: np.ndarray, origin_lonlat: tuple[float, float]) -> np.ndarray:
    """Convert lon/lat degrees to local equirectangular meters."""
    lon0, lat0 = origin_lonlat
    lat0_rad = math.radians(lat0)
    earth_r = 6371000.0
    lon = np.asarray(xy_lonlat[:, 0], dtype=float)
    lat = np.asarray(xy_lonlat[:, 1], dtype=float)
    xm = np.radians(lon - lon0) * earth_r * math.cos(lat0_rad)
    ym = np.radians(lat - lat0) * earth_r
    return np.column_stack([xm, ym])


def _local_m_to_lonlat(xy_m: np.ndarray, origin_lonlat: tuple[float, float]) -> np.ndarray:
    """Convert local equirectangular meters back to lon/lat degrees."""
    lon0, lat0 = origin_lonlat
    lat0_rad = math.radians(lat0)
    earth_r = 6371000.0
    x = np.asarray(xy_m[:, 0], dtype=float)
    y = np.asarray(xy_m[:, 1], dtype=float)
    lon = lon0 + np.degrees(x / (earth_r * math.cos(lat0_rad)))
    lat = lat0 + np.degrees(y / earth_r)
    return np.column_stack([lon, lat])


def _resolve_xy_mode(xy: np.ndarray, mode: str) -> str:
    mode = str(mode).strip().lower()
    if mode not in {"auto", "meter", "lonlat"}:
        raise ValueError(f"XY_MODE must be auto, meter, or lonlat. Got: {mode}")
    if mode == "auto":
        return "lonlat" if _is_lonlat_xy(xy) else "meter"
    return mode


def _metric_xy_from_original(xy: np.ndarray, mode: str, origin_lonlat: tuple[float, float] | None) -> np.ndarray:
    if mode == "meter":
        return np.asarray(xy, dtype=float)
    if origin_lonlat is None:
        raise ValueError("origin_lonlat is required for lonlat conversion")
    return _lonlat_to_local_m(np.asarray(xy, dtype=float), origin_lonlat)


def _finite_xy_mask(xy: np.ndarray) -> np.ndarray:
    return np.isfinite(xy[:, 0]) & np.isfinite(xy[:, 1])


