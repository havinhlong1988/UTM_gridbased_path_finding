def _collect_route_edge_file(params: SimpleNamespace) -> Path | None:
    """Return the preferred Theta* edge table, if enabled and available."""
    if not bool(pget(params, "USE_ROUTE_EDGE_FILE", True)):
        return None
    edge_file = Path(str(pget(params, "ROUTE_EDGE_FILE", "output/thetastar_master_plan/all_route_edges_v4.csv")))
    if edge_file.exists() and edge_file.is_file():
        return edge_file
    if bool(pget(params, "REQUIRE_ROUTE_EDGE_FILE", False)):
        raise FileNotFoundError(f"ROUTE_EDGE_FILE not found: {edge_file}")
    return None


def _required_edge_column(df: pd.DataFrame, names: Sequence[str], what: str) -> str:
    col = _find_column_exact(df, names)
    if col is None:
        raise ValueError(f"Missing required edge-table column for {what}. Tried: {list(names)}")
    return col


def _make_edge_route_instance_key(base_key: str, part_id: int, n_parts: int) -> str:
    """Build a stable route-instance key after splitting by edge_seq reset."""
    base_key = str(base_key or "route")
    if n_parts <= 1:
        return base_key
    return f"{base_key}__part_{int(part_id):05d}"


def _split_one_edge_group_by_seq_reset(
    group: pd.DataFrame,
    seq_col: str,
    params: SimpleNamespace,
) -> list[pd.DataFrame]:
    """Split one base route_key group into route instances.

    Important v6 fix
    ----------------
    In all_route_edges_v4.csv, ``route_key`` may describe the lane type
    (for example ``forward_main_01``) rather than one complete OD route.
    Therefore a single route_key can contain many routes.  The real route
    boundary is where ``edge_seq`` starts again from 0 or decreases.

    This function preserves the original CSV row order.  Do NOT sort the whole
    route_key group by edge_seq before splitting, because doing that mixes all
    edge_seq=0 rows from many routes, then all edge_seq=1 rows, etc.
    """
    if group.empty:
        return []

    g = group.sort_values("__original_row_order__", kind="mergesort").copy()
    seq = _coerce_numeric(g[seq_col]).to_numpy(dtype=float)

    reset = np.zeros(len(g), dtype=bool)
    reset[0] = True
    if len(g) > 1:
        prev = seq[:-1]
        cur = seq[1:]
        # New route when sequence returns to 0 or otherwise decreases/stalls.
        # ``cur <= prev`` catches reset 45->0 and duplicate/interleaved starts.
        reset[1:] = (cur <= prev) | (cur == 0)

    # Optional stricter split by node continuity.  This is disabled by default
    # because Theta*/LOS-smoothed route exports can sometimes skip sampled
    # cells, but from_node_id/to_node_id should still be continuous for a true
    # edge table.  Enable only for debugging a corrupted export.
    if bool(pget(params, "ROUTE_EDGE_SPLIT_ON_NODE_DISCONTINUITY", False)):
        from_col = _find_column_exact(g, pget(params, "ROUTE_EDGE_FROM_NODE_COLUMN_CANDIDATES", ["from_node_id", "from_idx", "from_node", "node1"]))
        to_col = _find_column_exact(g, pget(params, "ROUTE_EDGE_TO_NODE_COLUMN_CANDIDATES", ["to_node_id", "to_idx", "to_node", "node2"]))
        if from_col is not None and to_col is not None and len(g) > 1:
            from_ids = g[from_col].astype(str).to_numpy()
            to_ids = g[to_col].astype(str).to_numpy()
            node_gap = from_ids[1:] != to_ids[:-1]
            reset[1:] |= node_gap

    part_num = np.cumsum(reset).astype(int) - 1
    out: list[pd.DataFrame] = []
    for _, sub in g.assign(__edge_route_part__=part_num).groupby("__edge_route_part__", sort=False):
        if not sub.empty:
            out.append(sub.drop(columns=["__edge_route_part__"], errors="ignore"))
    return out


def _split_edge_table_into_route_groups(
    edge_df: pd.DataFrame,
    params: SimpleNamespace,
) -> tuple[list[tuple[str, pd.DataFrame]], str]:
    """Split all_route_edges_v4.csv into one DataFrame per actual route.

    Correct v6 logic:
        1. preserve original CSV row order;
        2. optionally separate by base ``route_key``;
        3. inside each base key, cut a new route whenever ``edge_seq`` resets
           to 0 or decreases;
        4. keep the sub-route in file order for reconstruction.

    Why this is necessary:
        ``route_key`` values such as ``forward_main_01`` can repeat for many
        OD routes.  Sorting the whole route_key group by edge_seq incorrectly
        connects all edge 0 rows together, then all edge 1 rows, creating fake
        disconnected route lines and corrupting hit-density.
    """
    key_col = _find_column_exact(
        edge_df,
        pget(
            params,
            "ROUTE_KEY_COLUMN_CANDIDATES",
            [
                "route_key", "route_id", "path_key", "path_id", "pair_key",
                "label_pair", "route_name", "pair_name", "od_key",
            ],
        ),
    )
    seq_col = _required_edge_column(
        edge_df,
        pget(params, "ROUTE_EDGE_SEQ_COLUMN_CANDIDATES", ["edge_seq", "seq", "edge_order"]),
        "edge sequence",
    )

    work = edge_df.copy()
    work[seq_col] = _coerce_numeric(work[seq_col])
    work = work[work[seq_col].notna()].copy()
    # Original row order must be assigned after filtering, then preserved.
    work["__original_row_order__"] = np.arange(len(work), dtype=int)
    work = work.sort_values("__original_row_order__", kind="mergesort")

    groups: list[tuple[str, pd.DataFrame]] = []

    if key_col is not None:
        base_note = f"base grouped by {key_col}; split inside each base key by {seq_col} reset/decrease; file order preserved"
        for base_key, base_group in work.groupby(key_col, sort=False, dropna=False):
            base_key_s = "" if pd.isna(base_key) else str(base_key)
            parts = _split_one_edge_group_by_seq_reset(base_group, seq_col, params)
            for i, part in enumerate(parts):
                rk = _make_edge_route_instance_key(base_key_s, i, len(parts))
                groups.append((rk, part))
        return groups, base_note

    # No route_key column. Split the whole file by edge_seq reset/decrease.
    parts = _split_one_edge_group_by_seq_reset(work, seq_col, params)
    for i, part in enumerate(parts):
        groups.append((f"edge_seq_route_{i:05d}", part))
    return groups, f"no route_key column; split whole edge table by {seq_col} reset/decrease; file order preserved"

def _edge_group_to_route_xy(
    group: pd.DataFrame,
    edge_file: Path,
    route_key: str,
    model: LoadedModel,
    params: SimpleNamespace,
) -> tuple[np.ndarray, np.ndarray, str]:
    """Reconstruct one route polyline from x1/y1 -> x2/y2 edge rows."""
    x1_col = _required_edge_column(group, pget(params, "ROUTE_EDGE_X1_COLUMN_CANDIDATES", ["x1", "from_x", "x_from"]), "edge x1")
    y1_col = _required_edge_column(group, pget(params, "ROUTE_EDGE_Y1_COLUMN_CANDIDATES", ["y1", "from_y", "y_from"]), "edge y1")
    x2_col = _required_edge_column(group, pget(params, "ROUTE_EDGE_X2_COLUMN_CANDIDATES", ["x2", "to_x", "x_to"]), "edge x2")
    y2_col = _required_edge_column(group, pget(params, "ROUTE_EDGE_Y2_COLUMN_CANDIDATES", ["y2", "to_y", "y_to"]), "edge y2")
    seq_col = _required_edge_column(group, pget(params, "ROUTE_EDGE_SEQ_COLUMN_CANDIDATES", ["edge_seq", "seq", "edge_order"]), "edge sequence")
    dist_col = _find_column_exact(group, pget(params, "ROUTE_EDGE_DISTANCE_COLUMN_CANDIDATES", ["edge_distance_m", "distance_m", "edge_length_m"]))

    g = group.copy()
    for c in [x1_col, y1_col, x2_col, y2_col, seq_col]:
        g[c] = _coerce_numeric(g[c])
    g = g.dropna(subset=[x1_col, y1_col, x2_col, y2_col, seq_col])
    if g.empty:
        raise ValueError(f"route_key={route_key}: no finite edge coordinates")

    # v6: keep the original file order after the edge_seq-reset split.
    # Sorting by edge_seq here would again mix duplicate 0..N sequences if a
    # base group was not perfectly cut.  Only use edge_seq sorting if explicitly
    # requested for a known one-route-per-key table.
    order_mode = str(pget(params, "ROUTE_EDGE_ORDER_MODE", "file_order_after_reset_split")).strip().lower()
    if order_mode in ("edge_seq", "seq", "sort_by_edge_seq"):
        g = g.sort_values([seq_col, "__original_row_order__"] if "__original_row_order__" in g.columns else [seq_col], kind="mergesort")
    elif "__original_row_order__" in g.columns:
        g = g.sort_values("__original_row_order__", kind="mergesort")

    raw_points = []
    first = g.iloc[0]
    raw_points.append([float(first[x1_col]), float(first[y1_col])])
    for _, row in g.iterrows():
        raw_points.append([float(row[x2_col]), float(row[y2_col])])
    xy_raw = np.asarray(raw_points, dtype=float)

    # Convert route coordinates into model-original and metric coordinate systems.
    xy_original, xy_metric, source_mode = _route_xy_to_model_spaces(xy_raw, x1_col, y1_col, model, params)

    # Check whether edge rows are connected. This is a route-key/edge-seq sanity check.
    # Use metric coordinates after conversion, so tolerance is in meters.
    disconnected = 0
    if len(g) >= 2:
        x1y1 = g[[x1_col, y1_col]].to_numpy(dtype=float)
        x2y2 = g[[x2_col, y2_col]].to_numpy(dtype=float)
        _, x1y1_metric, _ = _route_xy_to_model_spaces(x1y1, x1_col, y1_col, model, params)
        _, x2y2_metric, _ = _route_xy_to_model_spaces(x2y2, x2_col, y2_col, model, params)
        gap = np.linalg.norm(x1y1_metric[1:] - x2y2_metric[:-1], axis=1)
        tol = float(pget(params, "ROUTE_EDGE_CONNECT_TOLERANCE_M", 2.0))
        disconnected = int(np.sum(gap > tol))
        if disconnected > 0 and bool(pget(params, "REJECT_DISCONNECTED_ROUTE_EDGES", True)):
            raise ValueError(
                f"route_key={route_key}: disconnected ordered edges: {disconnected} gaps > {tol:.2f} m. "
                "Check route_key and edge_seq grouping."
            )

    validation_note = _validate_route_geometry(xy_metric, model, params, edge_file)

    # Optional route distance diagnostics from edge_distance_m, if present.
    distance_note = ""
    if dist_col is not None:
        edge_d = _coerce_numeric(g[dist_col]).dropna().to_numpy(dtype=float)
        if edge_d.size:
            distance_note = f", edge_distance_sum={float(np.nansum(edge_d)):.1f} m"

    return (
        xy_original,
        xy_metric,
        f"edge-table route_key={route_key}; edges={len(g)}; order={str(pget(params, 'ROUTE_EDGE_ORDER_MODE', 'file_order_after_reset_split'))}; "
        f"mode={source_mode}; disconnected_gaps={disconnected}{distance_note}{validation_note}",
    )


def _load_routes_from_edge_table(
    edge_file: Path,
    model: LoadedModel,
    params: SimpleNamespace,
) -> tuple[list[tuple[str, np.ndarray, np.ndarray, str]], list[LoadedRoute], str]:
    """Load all route polylines from all_route_edges_v4.csv."""
    edge_df = _read_table_flexible(edge_file)
    groups, grouping_note = _split_edge_table_into_route_groups(edge_df, params)
    routes: list[tuple[str, np.ndarray, np.ndarray, str]] = []
    skipped: list[LoadedRoute] = []

    min_route_points = int(pget(params, "MIN_ROUTE_POINTS", 2))
    for route_key, group in groups:
        try:
            xy_original, xy_metric, reason = _edge_group_to_route_xy(group, edge_file, route_key, model, params)
            if len(xy_metric) < min_route_points:
                raise ValueError(f"route_key={route_key}: too few route points after edge reconstruction: {len(xy_metric)}")
            routes.append((route_key, xy_original, xy_metric, reason))
        except Exception as exc:
            skipped.append(
                LoadedRoute(
                    path=edge_file,
                    xy_original=np.empty((0, 2)),
                    xy_metric=np.empty((0, 2)),
                    n_raw_points=0,
                    n_dense_points=0,
                    hit_nodes=0,
                    status="skipped",
                    reason=str(exc),
                    route_key=str(route_key),
                    source_kind="edge_table",
                )
            )
    return routes, skipped, grouping_note

def _extract_route_xy_from_df(
    route_df: pd.DataFrame,
    route_path: Path,
    model: LoadedModel,
    params: SimpleNamespace,
) -> tuple[np.ndarray, np.ndarray, str]:
    """Return route original and metric xy arrays.

    Important v3 fix: route geometry columns are matched strictly. This prevents
    summary columns such as start_x/end_x/center_x from being misread as a
    polyline, which would corrupt both route-line figures and hit-count density.
    """
    route_df, order_note = _sort_route_df_if_ordered(route_df, params)

    x_candidates, y_candidates = _route_xy_column_candidates(params)
    x_col = _find_column_exact(route_df, x_candidates)
    y_col = _find_column_exact(route_df, y_candidates)

    if x_col is not None and y_col is not None:
        x = _coerce_numeric(route_df[x_col])
        y = _coerce_numeric(route_df[y_col])
        xy_raw = np.column_stack([x.to_numpy(dtype=float), y.to_numpy(dtype=float)])
        finite = _finite_xy_mask(xy_raw)
        xy_raw = xy_raw[finite]
        xy_original, xy_metric, source_mode = _route_xy_to_model_spaces(xy_raw, x_col, y_col, model, params)
        validation_note = _validate_route_geometry(xy_metric, model, params, route_path)
        return xy_original, xy_metric, f"strict xy columns: {x_col}, {y_col}; mode={source_mode}{order_note}{validation_note}"

    # Try one node-index-per-row route format. Use strict matching only so
    # start_idx/end_idx in summary files are not misused as paths.
    idx_col = _find_column_exact(route_df, pget(params, "ROUTE_INDEX_COLUMN_CANDIDATES", []))
    if idx_col is not None:
        route_df, order_note = _sort_route_df_if_ordered(route_df, params)
        indices = _coerce_numeric(route_df[idx_col]).dropna().astype(int).tolist()
        xy_original, xy_metric = _indices_to_model_xy(indices, model, params)
        validation_note = _validate_route_geometry(xy_metric, model, params, route_path)
        return xy_original, xy_metric, f"strict index column: {idx_col}{order_note}{validation_note}"

    # Try single-row/list route format. Use strict matching only.
    list_col = _find_column_exact(route_df, pget(params, "ROUTE_INDEX_LIST_COLUMN_CANDIDATES", []))
    if list_col is not None:
        indices: list[int] = []
        for value in route_df[list_col].dropna().tolist():
            indices.extend(_parse_index_list(value))
        xy_original, xy_metric = _indices_to_model_xy(indices, model, params)
        validation_note = _validate_route_geometry(xy_metric, model, params, route_path)
        return xy_original, xy_metric, f"strict index-list column: {list_col}{validation_note}"

    raise ValueError(
        f"No strict route xy/index columns detected in {route_path.name}. "
        f"Columns found: {list(route_df.columns)}"
    )

def densify_polyline(xy_metric: np.ndarray, step_m: float) -> np.ndarray:
    """Densify a polyline in metric coordinates."""
    xy = np.asarray(xy_metric, dtype=float)
    if len(xy) < 2:
        return xy.copy()
    if step_m <= 0:
        return xy.copy()

    out = [xy[0]]
    for a, b in zip(xy[:-1], xy[1:]):
        if not (np.all(np.isfinite(a)) and np.all(np.isfinite(b))):
            continue
        dist = float(np.linalg.norm(b - a))
        if dist == 0:
            continue
        n_steps = max(1, int(math.ceil(dist / step_m)))
        for k in range(1, n_steps + 1):
            t = k / n_steps
            out.append(a * (1.0 - t) + b * t)
    return np.asarray(out, dtype=float)


