def build_eligible_mask(model: LoadedModel, params: SimpleNamespace) -> np.ndarray:
    df = model.df
    mask = _finite_xy_mask(model.xy_metric)

    exclude_prefixes = {str(v).upper() for v in _as_list(pget(params, "EXCLUDE_LABEL_PREFIXES", []))}
    exclude_labels = {str(v).upper() for v in _as_list(pget(params, "EXCLUDE_LABELS", []))}

    if exclude_prefixes:
        prefixes = df[model.label_prefix_col].fillna("").astype(str).str.upper()
        mask &= ~prefixes.isin(exclude_prefixes).to_numpy()

    if exclude_labels:
        labels = df[model.label_col].fillna("").astype(str).str.upper()
        mask &= ~labels.isin(exclude_labels).to_numpy()

    if bool(pget(params, "USE_ONLY_FLYABLE_MODEL_NODES", True)) and model.slowness_col is not None:
        slow = _coerce_numeric(df[model.slowness_col]).to_numpy(dtype=float)
        threshold = float(pget(params, "NOFLY_SLOWNESS_THRESHOLD", 10.0))
        mode = str(pget(params, "NOFLY_THRESHOLD_MODE", "greater_equal")).lower()
        if mode == "greater_equal":
            mask &= ~(slow >= threshold)
        elif mode == "greater":
            mask &= ~(slow > threshold)
        elif mode == "equal":
            mask &= ~(np.isclose(slow, threshold))
        else:
            raise ValueError(f"Unknown NOFLY_THRESHOLD_MODE: {mode}")

    return mask


def _count_hits_for_loaded_routes(
    loaded_routes: list[tuple[str, Path, np.ndarray, np.ndarray, str, str]],
    model: LoadedModel,
    params: SimpleNamespace,
    eligible_indices: np.ndarray,
    tree: cKDTree,
    hit_count: np.ndarray,
) -> tuple[list[LoadedRoute], list[LoadedRoute]]:
    """Count route hits for already reconstructed routes."""
    hit_buffer_m = float(pget(params, "HIT_BUFFER_M", 300.0))
    step_m = float(pget(params, "ROUTE_DENSIFY_STEP_M", 50.0))
    min_route_points = int(pget(params, "MIN_ROUTE_POINTS", 2))

    processed: list[LoadedRoute] = []
    skipped: list[LoadedRoute] = []

    for route_key, route_path, xy_original, xy_metric, reason, source_kind in loaded_routes:
        try:
            if len(xy_metric) < min_route_points:
                raise ValueError(f"too few route points after parsing: {len(xy_metric)}")

            dense = densify_polyline(xy_metric, step_m)
            if len(dense) < min_route_points:
                raise ValueError(f"too few dense route points: {len(dense)}")

            query = tree.query_ball_point(dense, r=hit_buffer_m)
            if len(query) == 0:
                local_hits = np.array([], dtype=int)
            else:
                non_empty = [np.asarray(q, dtype=int) for q in query if len(q) > 0]
                local_hits = np.unique(np.concatenate(non_empty)) if non_empty else np.array([], dtype=int)

            global_hits = eligible_indices[local_hits]
            hit_count[global_hits] += 1

            processed.append(
                LoadedRoute(
                    path=route_path,
                    xy_original=xy_original,
                    xy_metric=xy_metric,
                    n_raw_points=len(xy_metric),
                    n_dense_points=len(dense),
                    hit_nodes=int(len(global_hits)),
                    status="processed",
                    reason=reason,
                    route_key=str(route_key),
                    source_kind=source_kind,
                )
            )
        except Exception as exc:
            skipped.append(
                LoadedRoute(
                    path=route_path,
                    xy_original=np.empty((0, 2)),
                    xy_metric=np.empty((0, 2)),
                    n_raw_points=0,
                    n_dense_points=0,
                    hit_nodes=0,
                    status="skipped",
                    reason=str(exc),
                    route_key=str(route_key),
                    source_kind=source_kind,
                )
            )

    return processed, skipped


def compute_route_hits(
    model: LoadedModel,
    params: SimpleNamespace,
    eligible_mask: np.ndarray,
) -> tuple[np.ndarray, list[LoadedRoute], list[LoadedRoute]]:
    hit_count = np.zeros(len(model.df), dtype=np.int32)
    eligible_indices = np.flatnonzero(eligible_mask)
    if eligible_indices.size == 0:
        raise ValueError("No eligible model nodes remain after exclusion/flyability filters.")

    tree = cKDTree(model.xy_metric[eligible_indices])
    processed: list[LoadedRoute] = []
    skipped: list[LoadedRoute] = []

    # Preferred v5 logic: read all_route_edges_v4.csv and cut each route by route_key,
    # or by edge_seq reset if route_key is missing.
    edge_file = _collect_route_edge_file(params)
    if edge_file is not None:
        print(f"[kmean] Reading preferred route edge table: {edge_file}")
        routes, edge_skipped, grouping_note = _load_routes_from_edge_table(edge_file, model, params)
        print(f"[kmean] Edge table grouping: {grouping_note}")
        print(f"[kmean] Edge-table reconstructed routes: {len(routes):,}")
        loaded_routes = [
            (route_key, edge_file, xy_original, xy_metric, reason, "edge_table")
            for route_key, xy_original, xy_metric, reason in routes
        ]
        processed, skipped2 = _count_hits_for_loaded_routes(
            loaded_routes, model, params, eligible_indices, tree, hit_count
        )
        skipped = edge_skipped + skipped2
        if processed or bool(pget(params, "EDGE_FILE_DISABLE_LEGACY_FALLBACK", True)):
            if not processed:
                examples = "\n".join(f"  - {r.route_key or r.path.name}: {r.reason}" for r in skipped[:10])
                raise RuntimeError(
                    "No valid routes were reconstructed from ROUTE_EDGE_FILE.\n"
                    f"First skipped examples:\n{examples}"
                )
            return hit_count, processed, skipped

    # Legacy fallback: route CSV files with ordered x/y or model-index columns.
    route_files = _collect_route_files(params)
    if not route_files:
        raise FileNotFoundError(
            "No route edge table or legacy route CSV files found. Check ROUTE_EDGE_FILE and ROUTE_CSV_GLOBS."
        )

    print("[kmean] WARNING: using legacy per-route CSV reader; preferred all_route_edges_v4.csv was not used.")
    loaded_routes: list[tuple[str, Path, np.ndarray, np.ndarray, str, str]] = []
    for route_path in route_files:
        try:
            route_df = _read_table_flexible(route_path)
            xy_original, xy_metric, reason = _extract_route_xy_from_df(route_df, route_path, model, params)
            loaded_routes.append((route_path.stem, route_path, xy_original, xy_metric, reason, "legacy_csv"))
        except Exception as exc:
            skipped.append(
                LoadedRoute(
                    path=route_path,
                    xy_original=np.empty((0, 2)),
                    xy_metric=np.empty((0, 2)),
                    n_raw_points=0,
                    n_dense_points=0,
                    hit_nodes=0,
                    status="skipped",
                    reason=str(exc),
                    route_key=route_path.stem,
                    source_kind="legacy_csv",
                )
            )

    processed2, skipped2 = _count_hits_for_loaded_routes(
        loaded_routes, model, params, eligible_indices, tree, hit_count
    )
    processed += processed2
    skipped += skipped2

    if not processed:
        examples = "\n".join(f"  - {r.route_key or r.path.name}: {r.reason}" for r in skipped[:10])
        raise RuntimeError(
            "All route CSV files were skipped. Check route CSV columns.\n"
            f"First skipped examples:\n{examples}"
        )

    return hit_count, processed, skipped

