def load_model(params: SimpleNamespace) -> LoadedModel:
    model_file = Path(pget(params, "MODEL_FILE"))
    if not model_file.exists():
        raise FileNotFoundError(f"MODEL_FILE not found: {model_file}")

    df = _read_table_flexible(model_file)

    x_col = _find_column(df, pget(params, "X_COLUMN_CANDIDATES", ["x", "lon", "longitude"]))
    y_col = _find_column(df, pget(params, "Y_COLUMN_CANDIDATES", ["y", "lat", "latitude"]))
    z_col = _find_column(df, pget(params, "Z_COLUMN_CANDIDATES", ["z"]))
    slowness_col = _find_column(df, pget(params, "SLOWNESS_COLUMN_CANDIDATES", ["slowness"]))
    label_col = _find_column(df, pget(params, "LABEL_COLUMN_CANDIDATES", ["label"]))
    label_prefix_col = _find_column(df, pget(params, "LABEL_PREFIX_COLUMN_CANDIDATES", ["label_prefix"]))

    if x_col is None or y_col is None:
        raise ValueError(
            f"Could not detect x/y columns in model file {model_file}. "
            f"Columns found: {list(df.columns)}"
        )

    df[x_col] = _coerce_numeric(df[x_col])
    df[y_col] = _coerce_numeric(df[y_col])
    if z_col is not None:
        df[z_col] = _coerce_numeric(df[z_col])
    if slowness_col is not None:
        df[slowness_col] = _coerce_numeric(df[slowness_col])

    if label_col is None:
        label_col = "label"
        df[label_col] = ""
    if label_prefix_col is None:
        label_prefix_col = "label_prefix"
        df[label_prefix_col] = df[label_col].map(_derive_label_prefix)
    else:
        df[label_prefix_col] = df[label_prefix_col].fillna("").astype(str).str.upper()

    xy_original = df[[x_col, y_col]].to_numpy(dtype=float)
    mode = _resolve_xy_mode(xy_original, pget(params, "XY_MODE", "auto"))
    if mode == "lonlat":
        finite = _finite_xy_mask(xy_original)
        origin_lonlat = (float(np.nanmean(xy_original[finite, 0])), float(np.nanmean(xy_original[finite, 1])))
    else:
        origin_lonlat = None
    xy_metric = _metric_xy_from_original(xy_original, mode, origin_lonlat)

    return LoadedModel(
        df=df,
        x_col=x_col,
        y_col=y_col,
        z_col=z_col,
        slowness_col=slowness_col,
        label_col=label_col,
        label_prefix_col=label_prefix_col,
        xy_original=xy_original,
        xy_metric=xy_metric,
        metric_origin_lonlat=origin_lonlat,
        metric_mode=mode,
    )


def _collect_route_files(params: SimpleNamespace) -> list[Path]:
    globs = _as_list(pget(params, "ROUTE_CSV_GLOBS", "output/thetastar_master_plan/*.csv"))
    files: list[Path] = []
    for pattern in globs:
        files.extend(Path(p) for p in glob.glob(str(pattern), recursive=True))
    # Keep only real CSV files, unique, sorted.
    unique = sorted({p.resolve() for p in files if p.is_file() and p.suffix.lower() == ".csv"})
    return unique


def _parse_index_list(value) -> list[int]:
    if pd.isna(value):
        return []
    if isinstance(value, (list, tuple, np.ndarray)):
        return [int(v) for v in value]
    s = str(value).strip()
    if not s:
        return []
    try:
        parsed = ast.literal_eval(s)
        if isinstance(parsed, (list, tuple, np.ndarray)):
            return [int(v) for v in parsed]
    except Exception:
        pass
    # fallback: extract all integers from text like "1, 2, 3" or "[1 2 3]"
    return [int(v) for v in re.findall(r"-?\d+", s)]


def _indices_to_model_xy(
    indices: Sequence[int],
    model: LoadedModel,
    params: SimpleNamespace,
) -> tuple[np.ndarray, np.ndarray]:
    if len(indices) == 0:
        return np.empty((0, 2)), np.empty((0, 2))

    arr = np.asarray(indices, dtype=int)
    n = len(model.df)
    base = str(pget(params, "ROUTE_INDEX_BASE", "auto")).lower()
    if base == "one":
        arr = arr - 1
    elif base == "auto":
        # If all IDs are 1..n and no zero exists, treat as one-based.
        if arr.size > 0 and arr.min() >= 1 and arr.max() <= n and 0 not in set(arr.tolist()):
            # But if max < n, it may still be zero-based subset shifted by chance.
            # Prefer zero-based when every index is valid zero-based and at least one
            # index is close to 0 is absent. For route planners, Python usually writes
            # zero-based row indices, so only force one-based when max == n.
            if arr.max() == n:
                arr = arr - 1

    valid = (arr >= 0) & (arr < n)
    arr = arr[valid]
    if arr.size == 0:
        return np.empty((0, 2)), np.empty((0, 2))
    return model.xy_original[arr], model.xy_metric[arr]


def _route_xy_column_candidates(params: SimpleNamespace) -> tuple[list[str], list[str]]:
    """Return strict route x/y column candidates.

    These are intentionally separate from the model x/y candidates. The route
    reader must not use relaxed matching, because summary tables commonly have
    start_x/end_x/center_x columns that look like coordinates but are not an
    ordered route polyline.
    """
    default_x = [
        "route_x", "route_lon", "path_x", "path_lon", "node_x", "node_lon",
        "x", "lon", "longitude", "x_m", "path_x_m", "route_x_m", "node_x_m",
        "x_original", "easting", "east",
    ]
    default_y = [
        "route_y", "route_lat", "path_y", "path_lat", "node_y", "node_lat",
        "y", "lat", "latitude", "y_m", "path_y_m", "route_y_m", "node_y_m",
        "y_original", "northing", "north",
    ]
    return (
        list(pget(params, "ROUTE_X_COLUMN_CANDIDATES", default_x)),
        list(pget(params, "ROUTE_Y_COLUMN_CANDIDATES", default_y)),
    )


def _detect_route_xy_mode(
    xy_raw: np.ndarray,
    x_col: str,
    y_col: str,
    model: LoadedModel,
    params: SimpleNamespace,
) -> str:
    """Detect whether route columns are lon/lat or metric coordinates."""
    mode = str(pget(params, "ROUTE_XY_MODE", "auto")).strip().lower()
    if mode == "follow_model":
        return model.metric_mode
    if mode in {"meter", "lonlat"}:
        return mode
    if mode != "auto":
        raise ValueError("ROUTE_XY_MODE must be auto, follow_model, meter, or lonlat")

    nx = _norm_col(x_col)
    ny = _norm_col(y_col)
    lon_names = {"lon", "longitude", "route_lon", "path_lon", "node_lon"}
    lat_names = {"lat", "latitude", "route_lat", "path_lat", "node_lat"}
    metric_x_names = {"x_m", "path_x_m", "route_x_m", "node_x_m", "easting", "east"}
    metric_y_names = {"y_m", "path_y_m", "route_y_m", "node_y_m", "northing", "north"}

    if nx in lon_names or ny in lat_names:
        return "lonlat"
    if nx in metric_x_names or ny in metric_y_names:
        return "meter"

    # Generic x/y or path_x/path_y: infer from values.
    return _resolve_xy_mode(xy_raw, "auto")


def _route_xy_to_model_spaces(
    xy_raw: np.ndarray,
    x_col: str,
    y_col: str,
    model: LoadedModel,
    params: SimpleNamespace,
) -> tuple[np.ndarray, np.ndarray, str]:
    """Convert route xy into model-original plotting xy and metric xy."""
    source_mode = _detect_route_xy_mode(xy_raw, x_col, y_col, model, params)

    if source_mode == "lonlat":
        if model.metric_mode == "lonlat":
            origin = model.metric_origin_lonlat
            xy_metric = _metric_xy_from_original(xy_raw, "lonlat", origin)
            xy_original = xy_raw
        else:
            raise ValueError(
                "route columns look like lon/lat but model coordinates are metric; "
                "use a route file with model-index columns or matching metric x/y"
            )
    elif source_mode == "meter":
        xy_metric = np.asarray(xy_raw, dtype=float)
        if model.metric_mode == "lonlat" and model.metric_origin_lonlat is not None:
            # Make plotting consistent when PLOT_USE_ORIGINAL_XY=True.
            xy_original = _local_m_to_lonlat(xy_metric, model.metric_origin_lonlat)
        else:
            xy_original = xy_metric
    else:
        raise ValueError(f"unknown route coordinate mode: {source_mode}")

    return xy_original, xy_metric, source_mode


def _sort_route_df_if_ordered(route_df: pd.DataFrame, params: SimpleNamespace) -> tuple[pd.DataFrame, str]:
    order_col = _find_column_exact(
        route_df,
        pget(
            params,
            "ROUTE_ORDER_COLUMN_CANDIDATES",
            ["path_order", "route_order", "point_order", "node_order", "step", "seq", "sequence", "i"],
        ),
    )
    if order_col is None:
        return route_df, ""
    ordered = route_df.copy()
    ordered[order_col] = _coerce_numeric(ordered[order_col])
    ordered = ordered.sort_values(order_col, kind="mergesort")
    return ordered, f", ordered by {order_col}"


def _model_metric_bbox(model: LoadedModel, margin_m: float) -> tuple[float, float, float, float]:
    xy = model.xy_metric
    finite = _finite_xy_mask(xy)
    if finite.sum() == 0:
        raise ValueError("model has no finite metric xy coordinates")
    xmin = float(np.nanmin(xy[finite, 0])) - margin_m
    xmax = float(np.nanmax(xy[finite, 0])) + margin_m
    ymin = float(np.nanmin(xy[finite, 1])) - margin_m
    ymax = float(np.nanmax(xy[finite, 1])) + margin_m
    return xmin, xmax, ymin, ymax


def _validate_route_geometry(
    xy_metric: np.ndarray,
    model: LoadedModel,
    params: SimpleNamespace,
    route_path: Path,
) -> str:
    """Validate that a parsed route is an ordered path-like geometry."""
    if not bool(pget(params, "VALIDATE_ROUTE_GEOMETRY", True)):
        return ""

    min_unique = int(pget(params, "MIN_ROUTE_UNIQUE_POINTS", 2))
    rounded = np.round(np.asarray(xy_metric, dtype=float), decimals=3)
    unique_count = len(np.unique(rounded, axis=0)) if len(rounded) else 0
    if unique_count < min_unique:
        raise ValueError(f"too few unique route points: {unique_count}")

    hit_buffer_m = float(pget(params, "HIT_BUFFER_M", 300.0))
    margin_m = float(pget(params, "ROUTE_MODEL_BBOX_MARGIN_M", max(1000.0, 2.0 * hit_buffer_m)))
    xmin, xmax, ymin, ymax = _model_metric_bbox(model, margin_m)
    inside = (
        (xy_metric[:, 0] >= xmin)
        & (xy_metric[:, 0] <= xmax)
        & (xy_metric[:, 1] >= ymin)
        & (xy_metric[:, 1] <= ymax)
    )
    inside_fraction = float(np.mean(inside)) if len(inside) else 0.0
    min_inside_fraction = float(pget(params, "ROUTE_MIN_FRACTION_INSIDE_MODEL_BBOX", 0.95))
    if inside_fraction < min_inside_fraction:
        raise ValueError(
            f"route geometry outside model bbox: inside_fraction={inside_fraction:.2f}; "
            "this usually means the wrong x/y columns or coordinate mode were used"
        )

    if len(xy_metric) >= 2:
        seg = np.linalg.norm(np.diff(xy_metric, axis=0), axis=1)
        path_len = float(np.nansum(seg))
        min_len = float(pget(params, "ROUTE_MIN_PATH_LENGTH_M", 1.0))
        if path_len < min_len:
            raise ValueError(f"route path length too short: {path_len:.2f} m")

        max_seg = float(np.nanmax(seg)) if len(seg) else 0.0
        max_allowed = float(pget(params, "ROUTE_MAX_SEGMENT_M", 0.0))
        if bool(pget(params, "REJECT_ROUTES_WITH_LONG_JUMPS", False)) and max_allowed > 0 and max_seg > max_allowed:
            raise ValueError(
                f"route has suspicious long jump: max_segment={max_seg:.1f} m > {max_allowed:.1f} m"
            )
        if max_allowed > 0 and max_seg > max_allowed:
            return f", warning max_segment={max_seg:.1f} m"

    return ""



